def findmid(t1, t2):
    x = (t1[0] + t2[0]) / 2
    y = (t1[1] + t2[1]) / 2
    z = (t1[2] + t2[2]) / 2
    return x, y, z


def XYZtoxyY(X, Y, Z):
    a = X / (X + Y + Z)
    b = Y / (X + Y + Z)
    return a, b


